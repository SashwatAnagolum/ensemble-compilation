class EnergyEstimationWithZNEAndRCBenchmark(qiskit_benchmark.QiskitBenchmark):
    def __init__(self) -> None:
        self.pauli_measurement_indices = self.get_pauli_measurement_indices_beh2()
        self.num_pauli_measurement_bases = self.pauli_measurement_indices.shape[0]
        self.zne_repetitions = [0, 1, 2]
        self.num_frame_randomizations = 32
        self.num_zne_repetitions = len(self.zne_repetitions)
        self.num_circuits_per_measurement_basis = (
            self.num_zne_repetitions * self.num_frame_randomizations
        )

        num_circuits = (
            self.num_circuits_per_measurement_basis * self.num_pauli_measurement_bases
        )

        super().__init__(num_circuits, "Energy Estimation + ZNE + RC")

        self.num_qubits = 14
        self.num_layers = 4

        self.hea_params = np.random.sample((self.num_layers + 1, self.num_qubits, 2))

        i = qiskit.circuit.library.IGate()
        x = qiskit.circuit.library.XGate()
        y = qiskit.circuit.library.YGate()
        z = qiskit.circuit.library.ZGate()

        self.cnot_pauli_left_frames = [
            [i, i],
            [i, x],
            [i, y],
            [i, z],
            [x, i],
            [x, x],
            [x, y],
            [x, z],
            [y, i],
            [y, x],
            [y, y],
            [y, z],
            [z, i],
            [z, x],
            [z, y],
            [z, z],
        ]

        self.cnot_pauli_right_frames = [
            [i, i],
            [i, x],
            [z, y],
            [z, z],
            [x, x],
            [x, i],
            [y, z],
            [y, y],
            [y, x],
            [y, i],
            [x, z],
            [x, y],
            [z, i],
            [z, x],
            [i, y],
            [i, z],
        ]

        self.pauli_frame_randomization_indices = None

    def _get_circuit(self, index: int) -> qiskit.QuantumCircuit:
        circuit = qiskit.QuantumCircuit(self.num_qubits, self.num_qubits)

        measurement_basis = self.pauli_measurement_indices[
            index // self.num_circuits_per_measurement_basis
        ]

        num_loop_repetitions = self.zne_repetitions[
            (index % self.num_circuits_per_measurement_basis)
            // self.num_frame_randomizations
        ]

        self.pauli_frame_randomization_indices = np.random.randint(
            0, 16, (4, 2, 1 + 2 * num_loop_repetitions, 7)
        )

        for layer in range(self.num_layers):
            for qubit in range(self.num_qubits):
                circuit.ry(self.hea_params[layer, qubit, 0], qubit)

                for repetition in range(num_loop_repetitions):
                    circuit.ry(self.hea_params[layer, qubit, 0], qubit)
                    circuit.ry(-1 * self.hea_params[layer, qubit, 0], qubit)

                circuit.rz(self.hea_params[layer, qubit, 0], qubit)

                for repetition in range(num_loop_repetitions):
                    circuit.rz(self.hea_params[layer, qubit, 1], qubit)
                    circuit.rz(-1 * self.hea_params[layer, qubit, 1], qubit)

            # RC for CNOT layer (+ ZNE repetitions)
            for cnot_layer_index, (start, end) in enumerate(
                [(0, self.num_qubits), (1, self.num_qubits - 1)]
            ):
                for qubit in range(self.num_qubits):
                    circuit.append(
                        self.cnot_pauli_left_frames[
                            self.pauli_frame_randomization_indices[
                                layer, cnot_layer_index, 0, qubit // 2
                            ]
                        ][qubit % 2],
                        [qubit],
                    )

                for qubit in range(start, end, 2):
                    circuit.cx(qubit, qubit + 1)

                for qubit in range(self.num_qubits):
                    circuit.append(
                        self.cnot_pauli_right_frames[
                            self.pauli_frame_randomization_indices[
                                layer, cnot_layer_index, 0, qubit // 2
                            ]
                        ][qubit % 2],
                        [qubit],
                    )

                for repetition in range(num_loop_repetitions):
                    for sublayer_index in range(2):
                        for qubit in range(self.num_qubits):
                            circuit.append(
                                self.cnot_pauli_left_frames[
                                    self.pauli_frame_randomization_indices[
                                        layer,
                                        cnot_layer_index,
                                        repetition + 1,
                                        qubit // 2,
                                    ]
                                ][qubit % 2],
                                [qubit],
                            )

                        for qubit in range(start, end, 2):
                            circuit.cx(qubit, qubit + 1)

                        for qubit in range(self.num_qubits):
                            circuit.append(
                                self.cnot_pauli_right_frames[
                                    self.pauli_frame_randomization_indices[
                                        layer,
                                        cnot_layer_index,
                                        repetition + 2,
                                        qubit // 2,
                                    ]
                                ][qubit % 2],
                                [qubit],
                            )

        for qubit in range(self.num_qubits):
            circuit.ry(self.hea_params[self.num_layers, qubit, 0], qubit)

            for repetition in range(num_loop_repetitions):
                circuit.ry(self.hea_params[self.num_layers, qubit, 0], qubit)
                circuit.ry(-1 * self.hea_params[self.num_layers, qubit, 0], qubit)

            circuit.rz(self.hea_params[self.num_layers, qubit, 0], qubit)

            for repetition in range(num_loop_repetitions):
                circuit.rz(self.hea_params[self.num_layers, qubit, 1], qubit)
                circuit.rz(-1 * self.hea_params[self.num_layers, qubit, 1], qubit)

        circuit_construction_utils.add_local_pauli_measurements(
            circuit, list(range(self.num_qubits)), measurement_basis
        )

        return circuit

